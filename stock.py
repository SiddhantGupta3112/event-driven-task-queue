def send_report_email(to_email, ticker, pdf_path):
    """
    Send the generated PDF report as an email attachment via Resend's API.

    Reads RESEND_API_KEY from environment variables.

    Inputs:
        to_email (str): recipient email address.
        ticker (str): ticker symbol, used in subject/body.
        pdf_path (str): filesystem path to the PDF report to attach.

    Returns:
        bool: True if the email was sent successfully.

    Raises:
        RuntimeError: if RESEND_API_KEY is missing.
        requests.HTTPError: if the Resend API call fails.
    """
    api_key = os.environ.get("RESEND_API_KEY")
    if not api_key:
        raise RuntimeError("Missing required RESEND_API_KEY environment variable.")

    with open(pdf_path, "rb") as f:
        pdf_bytes = f.read()

    body = (
        f"Attached is the automated technical analysis report for {ticker}, "
        f"generated on {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}.\n\n"
        f"{DISCLAIMER_TEXT}\n"
    )

    response = requests.post(
        "https://api.resend.com/emails",
        headers={"Authorization": f"Bearer {api_key}"},
        json={
            "from": "onboarding@resend.dev",
            "to": [to_email],
            "subject": f"Automated Stock Report: {ticker}",
            "text": body,
            "attachments": [{
                "filename": os.path.basename(pdf_path),
                "content": base64.b64encode(pdf_bytes).decode(),
            }],
        },
        timeout=30,
    )
    response.raise_for_status()
    return True
