def calculate_rs_rsi(series, period=14):
    """
    Compute Relative Strength (RS) and Relative Strength Index (RSI) using
    Wilder's smoothing method.

    Inputs:
        series (pd.Series): price series (typically Close).
        period (int): lookback period for RSI calculation (default 14).

    Returns:
        tuple(pd.Series, pd.Series): (RS, RSI) aligned to the input index.
    """
    delta = series.diff()

    gain = delta.clip(lower=0)
    loss = -delta.clip(upper=0)

    avg_gain = gain.ewm(alpha=1 / period, min_periods=period, adjust=False).mean()
    avg_loss = loss.ewm(alpha=1 / period, min_periods=period, adjust=False).mean()

    rs = avg_gain / avg_loss
    rsi = 100 - (100 / (1 + rs))

    return rs, rsi


def calc_slope(series, window=5):
    """
    Compute a rolling linear-regression slope of a series.

    Inputs:
        series (pd.Series): numeric series to compute the trend slope on.
        window (int): rolling window size (default 5).

    Returns:
        pd.Series: rolling slope values aligned to the input index.
    """
    return series.rolling(window).apply(
        lambda x: linregress(range(len(x)), x)[0]
    )


def feature_generation(df):
    """
    Generate the full set of technical-analysis features used by the model
    and reports, exactly preserving the original feature engineering logic.

    Inputs:
        df (pd.DataFrame): raw OHLCV dataframe as returned by yf.download,
            indexed by date, with columns including Open, High, Low, Close,
            Volume.

    Returns:
        pd.DataFrame: the input dataframe with added columns for moving
            averages, MACD/signal/histogram, RSI, Bollinger Bands, volume
            change, ROC, OBV, stochastic %K/%D, rolling 3/5/10-day returns,
            trend slope, lagged close/volume, and the binary Target column
            (1 if Close > Open, else 0). Rows with any NaN (from rolling
            windows) are dropped.
    """
    df.columns = [
        col[0] if isinstance(col, tuple) and col[1] != ''
        else col if isinstance(col, str)
        else ''.join(col)
        for col in df.columns
    ]

    # MA Creation
    df['50DSMA'] = df['Close'].rolling(window=50).mean()
    df['200DSMA'] = df['Close'].rolling(window=200).mean()
    df['8DEMA'] = df['Close'].ewm(span=8, adjust=False).mean()

    # MACD Creation
    df['26DEMA'] = df['Close'].ewm(span=26, adjust=False).mean()
    df['12DEMA'] = df['Close'].ewm(span=12, adjust=False).mean()
    df['MACD'] = df['12DEMA'] - df['26DEMA']
    df['Signal'] = df['MACD'].ewm(span=9, adjust=False).mean()
    df['MACD_Hist'] = df['MACD'] - df['Signal']

    # RSI Creation
    rs, rsi = calculate_rs_rsi(df['Close'], period=14)
    df['RS'] = rs
    df['RSI_14'] = rsi

    # Bollinger Band Creation
    df['BB_middle'] = df['Close'].rolling(window=20).mean()
    df['BB_std'] = df['Close'].rolling(window=20).std()
    df['BB_upper'] = df['BB_middle'] + 2 * df['BB_std']
    df['BB_lower'] = df['BB_middle'] - 2 * df['BB_std']

    # Volume Difference
    df['Volume_Change'] = df['Volume'].diff()

    # Rate of Change Creation
    df['ROC'] = df['Close'].pct_change(periods=10)

    # On Balance Volume Creation
    df['Direction'] = np.where(df['Close'] > df['Close'].shift(1), 1, -1)
    df['Direction'] = np.where(df['Close'] == df['Close'].shift(1), 0, df['Direction'])
    df['OBV'] = (df['Volume'] * df['Direction']).cumsum()

    # Stochastic Oscillator Creation
    low_min = df['Low'].rolling(window=14).min()
    high_max = df['High'].rolling(window=14).max()
    df['%K'] = 100 * ((df['Close'] - low_min) / (high_max - low_min))
    df['%D'] = df['%K'].rolling(window=3).mean()

    # Rolling Returns
    df['Return_3D'] = df['Close'].pct_change(periods=3)
    df['Return_5D'] = df['Close'].pct_change(periods=5)
    df['Return_10D'] = df['Close'].pct_change(periods=10)

    # Trend Slope
    df['Trend_Slope'] = calc_slope(df['Close'])

    # Lagged Features
    df['Lagged_Close'] = df['Close'].shift(1)
    df['Lagged_Volume'] = df['Volume'].shift(1)

    # Target Creation
    df['Target'] = (df['Close'] > df['Open']).astype(int)

    df = df.dropna()

    return df


