# Feature columns used to train and predict with the classifier.
_MODEL_FEATURES = [
    '50DSMA', '200DSMA', '8DEMA', 'MACD', 'Signal', 'MACD_Hist',
    'RSI_14', 'BB_upper', 'BB_middle', 'BB_lower', 'Volume_Change',
    'ROC', 'OBV', '%K', '%D', 'Return_3D', 'Return_5D', 'Return_10D',
    'Trend_Slope', 'Lagged_Close', 'Lagged_Volume',
]


def train_and_predict(df):
    """
    Train a simple Logistic Regression classifier on engineered features to
    predict next-day direction (Target: Close > Open), and predict the
    direction for the most recent row.

    The most recent row is excluded from training since it has no realized
    Target value yet (it represents "today"). Training uses a lightweight
    classical model so the whole operation completes in well under 5 seconds
    even for several thousand rows of daily data.

    Inputs:
        df (pd.DataFrame): feature-engineered dataframe as produced by
            feature_generation, including the Target column and all columns
            listed in _MODEL_FEATURES.

    Returns:
        dict: {
            "predicted_direction": "up" or "down",
            "confidence": float, the model's predict_proba for the
                predicted class,
            "feature_values": dict of feature name -> value used for the
                prediction (the most recent row's features),
        }

    Raises:
        ValueError: if there are not enough rows to train a model.
    """
    if len(df) < 2:
        raise ValueError(
            "Not enough data rows to train a model and make a prediction."
        )

    train_df = df.iloc[:-1]
    latest_row = df.iloc[[-1]]

    X_train = train_df[_MODEL_FEATURES]
    y_train = train_df['Target']

    if y_train.nunique() < 2:
        raise ValueError(
            "Training data contains only one class of Target values; "
            "cannot train a classifier."
        )

    model = LogisticRegression(max_iter=1000)
    model.fit(X_train, y_train)

    X_latest = latest_row[_MODEL_FEATURES]
    predicted_class = model.predict(X_latest)[0]
    probabilities = model.predict_proba(X_latest)[0]
    class_index = list(model.classes_).index(predicted_class)
    confidence = float(probabilities[class_index])

    predicted_direction = "up" if predicted_class == 1 else "down"

    feature_values = X_latest.iloc[0].to_dict()

    return {
        "predicted_direction": predicted_direction,
        "confidence": confidence,
        "feature_values": feature_values,
    }


