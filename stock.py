def data_plotting(df):
    """
    Build a set of interactive Plotly charts (closing price, volume overlay,
    moving averages, candlestick, MACD/signal, MACD histogram, RSI,
    Bollinger Bands) for an interactive frontend chart view.

    This function is preserved for optional future use (e.g. an interactive
    frontend) but is NOT called by generate_stock_report.

    Inputs:
        df (pd.DataFrame): feature-engineered dataframe as produced by
            feature_generation.

    Returns:
        dict: mapping of chart name -> Plotly figure JSON string
            (via plotly.io.to_json), keyed by: closing_price, volume_overlay,
            moving_averages, candlestick, macd_signal, macd_histogram, rsi,
            bollinger_bands.
    """
    graphs = {}

    end_date_data = df.index.max()
    start_date_segment = max(df.index.min(), end_date_data - timedelta(days=365))

    common_layout: dict[str, Any] = dict(
        plot_bgcolor='white',
        paper_bgcolor='white',
        font=dict(family="Segoe UI, sans-serif", size=13, color="#222"),
        margin=dict(t=100, l=50, r=30, b=50),
        hovermode="x unified",
        height=500,
        xaxis=dict(
            showgrid=True,
            gridcolor='lightgrey',
            linecolor='darkgrey',
            linewidth=1,
            rangeslider=dict(visible=True, thickness=0.05),
            rangeselector=dict(
                buttons=list([
                    dict(count=1, label="1m", step="month", stepmode="backward"),
                    dict(count=6, label="6m", step="month", stepmode="backward"),
                    dict(count=1, label="YTD", step="year", stepmode="todate"),
                    dict(count=1, label="1y", step="year", stepmode="backward"),
                    dict(step="all")
                ]),
                y=1.02,
                yanchor="bottom"
            ),
            range=[start_date_segment, end_date_data],
            fixedrange=False
        ),
        yaxis=dict(
            showgrid=True,
            gridcolor='lightgrey',
            linecolor='darkgrey',
            linewidth=1,
            zeroline=False,
            fixedrange=False
        )
    )

    # 1. Closing Price
    fig1 = go.Figure()
    fig1.add_trace(go.Scatter(x=df.index, y=df['Close'], mode='lines', name='Close',
                              line=dict(color='#1f77b4', width=2)))
    fig1.update_layout(title="Closing Price", yaxis_title="Price (INR)", **common_layout)
    graphs['closing_price'] = pio.to_json(fig1)

    # 2. Volume Overlay
    fig2 = go.Figure()
    fig2.add_trace(go.Scatter(x=df.index, y=df['Close'], name='Close',
                              line=dict(color='#1f77b4', width=2), yaxis="y1"))
    fig2.add_trace(go.Bar(x=df.index, y=df['Volume'], name='Volume',
                          marker=dict(color='rgba(128,128,128,0.4)'), yaxis="y2"))

    volume_layout = common_layout.copy()
    volume_layout.pop('yaxis', None)

    fig2.update_layout(
        title="Volume Overlay",
        yaxis=dict(title="Price (INR)", side='left', fixedrange=False,
                  showgrid=True, gridcolor='lightgrey', linecolor='darkgrey', linewidth=1, zeroline=False),
        yaxis2=dict(title="Volume", overlaying="y", side="right", showgrid=False, fixedrange=False),
        barmode='overlay',
        **volume_layout
    )
    graphs['volume_overlay'] = pio.to_json(fig2)

    # 3. Moving Averages
    fig3 = go.Figure()
    fig3.add_trace(go.Scatter(x=df.index, y=df['Close'], name='Close', line=dict(color='black')))
    fig3.add_trace(go.Scatter(x=df.index, y=df['50DSMA'], name='50 DSMA', line=dict(color='orange')))
    fig3.add_trace(go.Scatter(x=df.index, y=df['200DSMA'], name='200 DSMA', line=dict(color='green')))
    fig3.add_trace(go.Scatter(x=df.index, y=df['8DEMA'], name='8 DEMA', line=dict(color='red')))
    fig3.update_layout(title="Moving Averages", yaxis_title="Price (INR)", **common_layout)
    graphs['moving_averages'] = pio.to_json(fig3)

    # 4. Candlestick
    fig4 = go.Figure(data=[go.Candlestick(
        x=df.index,
        open=df['Open'], high=df['High'], low=df['Low'], close=df['Close'],
        increasing=dict(line=dict(color="green", width=1.5)),
        decreasing=dict(line=dict(color="red", width=1.5))
    )])
    fig4.update_layout(title="Candlestick Chart", yaxis_title="Price (INR)", **common_layout)
    graphs['candlestick'] = pio.to_json(fig4)

    # 5. MACD & Signal
    fig5 = go.Figure()
    fig5.add_trace(go.Scatter(x=df.index, y=df['MACD'], name='MACD', line=dict(color='red')))
    fig5.add_trace(go.Scatter(x=df.index, y=df['Signal'], name='Signal', line=dict(color='blue')))
    fig5.update_layout(title="MACD & Signal Line", yaxis_title="Value", **common_layout)
    graphs['macd_signal'] = pio.to_json(fig5)

    # 6. MACD Histogram
    pos = df['MACD_Hist'].copy()
    pos[pos < 0] = np.nan
    neg = df['MACD_Hist'].copy()
    neg[neg >= 0] = np.nan

    fig6 = go.Figure()
    fig6.add_trace(go.Bar(x=df.index, y=pos, name="Positive", marker=dict(color='rgba(44,160,44,0.7)')))
    fig6.add_trace(go.Bar(x=df.index, y=neg, name="Negative", marker=dict(color='rgba(214,39,40,0.7)')))
    fig6.update_layout(title="MACD Histogram", yaxis_title="MACD Value", barmode="overlay", **common_layout)
    graphs['macd_histogram'] = pio.to_json(fig6)

    # 7. RSI
    fig7 = go.Figure()
    fig7.add_trace(go.Scatter(x=df.index, y=df['RSI_14'], name='RSI (14)', line=dict(color='purple')))
    fig7.add_shape(type="line", x0=start_date_segment, x1=end_date_data, y0=70, y1=70,
                   line=dict(color='red', dash='dash'))
    fig7.add_shape(type="line", x0=start_date_segment, x1=end_date_data, y0=30, y1=30,
                   line=dict(color='green', dash='dash'))

    rsi_layout = common_layout.copy()
    rsi_layout.pop('yaxis', None)

    fig7.update_layout(
        title="RSI (14)",
        yaxis=dict(title="RSI", range=[0, 100], fixedrange=False,
                  showgrid=True, gridcolor='lightgrey', linecolor='darkgrey', linewidth=1, zeroline=False),
        **rsi_layout
    )
    graphs['rsi'] = pio.to_json(fig7)

    # 8. Bollinger Bands
    fig8 = go.Figure()
    fig8.add_trace(go.Scatter(x=df.index, y=df['Close'], name='Close', line=dict(color='black')))
    fig8.add_trace(go.Scatter(x=df.index, y=df['BB_upper'], name='Upper Band', line=dict(color='brown', dash='dash')))
    fig8.add_trace(go.Scatter(x=df.index, y=df['BB_middle'], name='Middle Band', line=dict(color='gray')))
    fig8.add_trace(go.Scatter(x=df.index, y=df['BB_lower'], name='Lower Band', line=dict(color='brown', dash='dash')))
    fig8.update_layout(title="Bollinger Bands", yaxis_title="Price (INR)", **common_layout)
    graphs['bollinger_bands'] = pio.to_json(fig8)

    return graphs