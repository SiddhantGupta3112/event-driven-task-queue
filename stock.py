def fetch_stock_data(ticker, start="2015-01-01", end=None):
    """
    Download historical OHLCV data for a ticker via yfinance and validate it.

    Inputs:
        ticker (str): ticker symbol to fetch (e.g. "AAPL").
        start (str): start date in YYYY-MM-DD format (default "2015-01-01").
        end (str or None): end date in YYYY-MM-DD format. Defaults to
            yesterday if not provided.

    Returns:
        pd.DataFrame: raw OHLCV dataframe.

    Raises:
        ValueError: if the ticker is invalid or yfinance returns no data.
    """
    if end is None:
        end = (date.today() - timedelta(days=1)).isoformat()

    data = yf.download(ticker, start=start, end=end)

    if data is None or data.empty:
        raise ValueError(
            f"No data returned for ticker '{ticker}'. The ticker may be "
            f"invalid, delisted, or have no trading history in the "
            f"requested date range."
        )

    return data


